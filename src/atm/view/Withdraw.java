    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       // Deposit
    if (jTextField1.getText().isEmpty()){
          JOptionPane.showMessageDialog(null, "Please enter all fields");
    }
    else{
    try {
      Card card = this.atm.getCurrentCard();
      long withdrawAmount = Long.parseLong(this.jTextField1.getText());
      this.atm.withdrawAtmCard(card, withdrawAmount);
      JOptionPane.showMessageDialog(null,"Withdraw operation is completed");
    } catch (AtmOperationException e) {
      Logger.getLogger(Withdraw.class.getName()).log(Level.SEVERE, e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed
    }
